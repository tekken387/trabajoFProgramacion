    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
         tableDestinos.clearSelection();
        tableModel.setRowCount(0);
        
        limpiaTxt();
        
        if(this.cuentaDestinos()<1){
            JOptionPane.showMessageDialog(this,"No hay Destinos para mostrar");
        }else{
            for(Destino d:getLtaDestinos()){
                Object[] data={d.getId(),d.getDescripcion()};
                tableModel.addRow(data);
            }

        }
        
        btnGuardar.setEnabled(false);
        btnNuevo.setEnabled(true);
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        btnNuevo.setEnabled(false);
        btnGuardar.setEnabled(true);
        if(this.cuentaDestinos()<getLtaDestinos().length){
            limpiaTxt();
            txtDescripcion.setEnabled(true);
            txtId.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this,"Se alcanzo el tamaÃ±o maximo de destinos permitidos");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Destino d=new Destino();
        String descripcion=txtDescripcion.getText();
        String id=txtId.getText();

        int pos=cuentaDestinos();
        
        if(!"".equals(descripcion) && id.matches("([0-9]+)")){
            d.setDescripcion(descripcion);
            d.setId(Integer.parseInt(id));
            getLtaDestinos()[pos]=d;
            
            //lo guardamos en la ventana principal para poder manejarlo en cualquier ventana  
            p.setLtaDestinos(getLtaDestinos());
            JOptionPane.showMessageDialog(this,"Destino guardado correctamente....");
            
            txtId.setText("");
            txtDescripcion.setText("");
            txtId.setEnabled(false);
            txtDescripcion.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnNuevo.setEnabled(true);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    public int cuentaDestinos(){
        int ndestinos=0;
        for (int i = 0; i < getLtaDestinos().length; i++) {
            if(getLtaDestinos()[i]!=null){
                ndestinos++;
            }
        }
        return ndestinos;
    }
    
